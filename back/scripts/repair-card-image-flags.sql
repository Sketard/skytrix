-- Ce script remet `local = false` pour ces 156 cartes. Après exécution,
-- relancer `PUT /api/parameters/update/images` rattrape les fichiers manquants.
--
-- NON versionné en migration Flyway : incohérence locale ponctuelle, pas un
-- changement de schéma à propager. À exécuter à la main :
--   psql -h localhost -U postgres -d skytrix -f back/scripts/repair-card-image-flags.sql
